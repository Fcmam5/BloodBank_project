    public Medecin getMedecin (){
        
        int identifiant = Integer.parseInt(idMed.getText().trim());
        Adresse adrMed=new Adresse(dayraMed.getText(),wilayaMed.getText());
       
        
        Medecin med;
        med = new Medecin(
                identifiant,
                nameMed.getText(),
                firstNameMed.getText(),
                mailMed.getText(),
                phoneMed.getText(),
                adrMed,
                dateIntegrationMed.getText()
        );
        
        
      
        return med;
    }
    
